#include <stdlib.h>
#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char		*map;
	t_size_t	i;

	map = (char *)malloc((ft_strlen(s) + 1) * sizeof(char));
	if (!map)
		return (FT_NULL);
	i = 0;
	while (s[i])
	{
		map[i] = f(i, s[i]);
		i++;
	}
	map[i] = '\0';
	return (map);
}
